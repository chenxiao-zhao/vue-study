<template>
  <div class="confirm-button">
    <button @click="getButtonClick">{{text|'чбошод'}}</button>
  </div>
</template>

<script>
export default {
  name: "confirm-button",
  props: ["text"],
  data() {
    return {
      msg: true
    };
  },
  methods: {
    getButtonClick() {
      this.$emit("message", this.msg);
    }
  }
};
</script>
